#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	const size_t res = ft_strlen(src);

	if (res + 1 < size)
		ft_memcpy(dest, src, res + 1);
	else if (size != 0)
	{
		ft_memcpy(dest, src, size - 1);
		dest[size - 1] = '\0';
	}
	return (res);
}
